package main.ch2;

public class PrimitiveTypeEx1 {
    public static void main(String[] args) {
        boolean power = true;
        boolean checked = false;

        // 자바에서는 대소문자가 구분되기에 TRUE true 다른 것
        System.out.println("power = " + power);
        System.out.println("checked = " + checked);
    }
}
